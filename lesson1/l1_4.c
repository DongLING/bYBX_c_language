#include <stdio.h>
int main()
{
    printf("123456789012345678901234567890\n");
    printf("123456781234567812345678\n");
    printf("123\t456\t789\n");
    printf("12345\n");
    printf("12345\b123\n");
    printf("12345\b123\r9\n");
    return 0;
}
