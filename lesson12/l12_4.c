 /*----------------------------------------------------------
  程序L12_4.C功能：引用共用体变量的成员
 -----------------------------------------------------------*/
 void main()
 {
  union  temp
    { char a;
      int b;
    }t;
  t.a=66;
  t.b=266;
  /*266=256+10即266的二进制为1 00001010，所以高字节放1、低字节放10*/
  printf("%x:%d, %x:%d\n",&t.a,t.a,&t.b,t.b);
}
