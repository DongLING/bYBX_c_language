/*-----------------------------------------------------------
  程序L2_6.C : 从键盘输入一个大写字母，输出其对应的小写字母。
-------------------------------------------------------------*/

#include <stdio.h>

int main( )
{
    char c;
    printf("请输一个数字字符(0-9):\n");
    scanf("%c",&c);
    c=c-48;         /*数字字符(0-9)的ASCII码减去48为其对应的十进制数*/
    printf("其十进制数为:%d\n",c);
    return 0;
}
