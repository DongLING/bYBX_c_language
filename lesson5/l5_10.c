 /*--------------------------------------------------------------------
  程序L5_10.C功能：用条件运算符实现输入一个英文字符，输出它的大写形式。
  -------------------------------------------------------------------*/
 #include <stdio.h>
 void main()
 {
  char ch;
  scanf("%c",&ch);
  ch=(ch>='a'&&ch<='z')?ch-32:ch;
  printf("%c\n",ch);
 }
