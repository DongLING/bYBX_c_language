  /*--------------------------------------------------------
  程序L3_3.C功能：由键盘输入x、y两个数，要求从大到小排序并输出。
  ----------------------------------------------------------*/
 #include <stdio.h>
 void main()                   /*主函数无返回值时使用空类型void*/
 {
  float x,y,t;
  scanf ("%f,%f",&x,&y);
  if (x<y)
     {t=x;x=y;y=t;}
  printf("%6.2f,%6.2f\n",x,y);
 }
