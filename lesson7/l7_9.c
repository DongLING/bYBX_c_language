 /*------------------------------------------------------
  程序L7_9.C : 求Fibonacci数列的前20项。
 -------------------------------------------------------*/
 #include <stdio.h>
 void main( )                     
 {
  int i , f[20]={1,1};  /*给出Fibonacci数列的前2项*/
  /*根据公式由Fibonacci数列前2项求出其余18项*/
  for(i=2;i<20;i++)
     f[i]=f[i-1]+f[i-2];
  /*输出Fibonacci数列的前20项*/
  for(i=0;i<20;i++)
     { if (i%5==0)  printf("\n");  /*控制一行输出5个元素*/
       printf("%10d",f[i]);
      }
 }
