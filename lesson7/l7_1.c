 /*-----------------------------------------------------------------
  程序L7_1.C功能：将数字0~4放入一个整型数组，并逆序输出数组。
  ------------------------------------------------------------------*/
 #include <stdio.h>
 void main()    
 { 
  int i,a[5];
  /*给数组中元素赋值*/
  for (i=0;i<5;i++)
       a[i]=i;
  /*逆序输出数组中元素值*/
  for (i=4;i>=0;i--)
     printf ("%3d",a[i]);
  printf("\n");
 }

