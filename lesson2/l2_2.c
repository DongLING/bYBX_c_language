 /*-------------------------------------------------------------------
程序L2_2.C功能：将字符'A' 赋值给字符变量的四种方法。
--------------------------------------------------------------------*/
#include <stdio.h>

int main()
{
     char c1,c2,c3,c4;
     c1='A';
     c2=65;
     c3='\101';
     c4='\x41';
     printf("c1=%c,c2=%c,c3=%c,c4=%c\n",c1,c2,c3,c4);
     printf("c1=%d,c2=%d,c3=%d,c4=%d\n",c1,c2,c3,c4);
     return 0;
}
