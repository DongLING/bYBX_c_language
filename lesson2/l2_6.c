/*-----------------------------------------------------------
  程序L2_6.C : 从键盘输入一个大写字母，输出其对应的小写字母。
-------------------------------------------------------------*/

#include <stdio.h>

int main( )
{
    char c;
    printf("请输入大写字母\n");
    scanf("%c",&c);
    c=c+32;         /*大写字母的ASCII码加上32为其对应的小写字母*/
    printf("其小写字母为%c\n",c);
    return 0;
}
