 /*--------------------------------------------------------
 程序L10 4.C功能：输出数组中的全部元素 
 --------------------------------------------------------*/
 #include <stdio.h>
 void main()
 {
  int *p,i;
  int a[5]={1,2,3,4,5};
  p=a;
  for (i=0;i<5;i++)
    printf("%d\t ",a[i]);
  printf("\n");
  for (i=0;i<5;i++)
    printf("%d\t ",*(p+i));
 }
