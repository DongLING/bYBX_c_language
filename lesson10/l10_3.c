 #include <stdio.h>
 void swap (int *p1,int *p2)
 { int p;
   p=*p1;
   *p1=*p2;
   *p2=p;
  }

 void main()
 {
  int a=5,b=9;
  int *ptr1,*ptr2;
  ptr1=&a; ptr2=&b;
  if (a<b)  swap(ptr1,ptr2);
  printf("%d,%d\n",a,b);
 }
