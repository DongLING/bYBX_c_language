 /*-----------------------------------------
  程序L4_4.C功能：判断一个数是否是素数。
  ------------------------------------------*/
 #include <stdio.h>
 void main()         
 {
  int n,i;
  scanf("%d",&n);
  for (i=2;i<=n-1;i=i+1)
     if (n%i==0) break;      /*运算符"%"结果是两个整数的余数*/
  if (i<n)    printf("%d不是素数\n",n);
  else  printf("%d是素数\n",n);
}
