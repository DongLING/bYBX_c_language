 /*------------------------------------------------------------
  程序L4_3.C功能：求1+2+… …+100的结果。
  -------------------------------------------------------------*/
 #include <stdio.h>
 void main()         
 {
  int s,n;
  for(s=0,n=1;n<=100;n=n+1)
     s=s+n;
  printf("1+2+3+… ...+100=%d\n",s);
 }

