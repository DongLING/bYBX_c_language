 /*-------------------------------------------------
   程序L4_1.C功能：求1+2+… …+100的结果。
   -------------------------------------------------*/
 #include <stdio.h>
 void main()         
 {
  int s,n;
  s=0;
  n=1;
  while (n<=100)   /*当n等于101时结束循环*/
    {s=s+n;
     n=n+1;        /*循环变量n用于改变循环条件，保证循环可以结束*/
    }
  printf("1+2+3+… ...+100=%d\n",s);
 }
