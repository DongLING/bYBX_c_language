 /*-------------------------------------------- 
   程序L4_2.C功能：求1+2+… …+100的结果。
   --------------------------------------------*/
 #include <stdio.h>
 void main()         
 {
  int s,n;
  s=0;
  n=1;
  do
    {s=s+n;
     n=n+1;      
     }
  while (n<=100);    
  printf("1+2+3+… ...+100=%d\n",s);
 }
