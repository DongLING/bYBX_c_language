 /*--------------------------------------------------------------
 程序L4_9.C功能：计算1-1/3+1/5-1/7+…直到最后一项的绝对值小于1e-6。
 ---------------------------------------------------------------*/
 #include <stdio.h>
 void main()         
 {
  int n=1;
  float x=1,t=1,s=0;          /*x控制通项正负号，t为通项值*/
  while (t>=1e-6)
    { t=1.0/(2*n-1);
      s=s+x*t;
      x= (-1)*x;
      n=n+1;
     }
  printf("1-1/3+1/5-1/7+… =%f\n",s);
   }
