 /*------------------------------------------------------------
   程序L4_5.C功能：统计1~50之间不能被3整除的数的个数并输出这些数。
  ------------------------------------------------------------*/
 #include <stdio.h>
 void main()         
 {
  int n,s;
  for (n=1,s=0;n<=20;n=n+1)
    { if (n%3==0)  continue;
      printf("%d\t",n);
      s=s+1;
     }
  printf("\ntotal:%d\n",s);
  }
